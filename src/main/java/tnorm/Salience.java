package tnorm;

public class Salience {

    private int salience;
    private final int maxSalience = 2;

    public int getSalience() {
        return salience;
    }

    public void setSalience(int salience) {
        this.salience = salience;
    }

    public int getMaxSalience() {
        return maxSalience;
    }
}
